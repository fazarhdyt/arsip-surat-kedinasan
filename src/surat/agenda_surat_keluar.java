            prepStat = conn.prepareStatement(sqlQuery);
            prepStat.setString(1, "%" + no1.getText() + "%");
            res = prepStat.executeQuery();
            
            DefaultTableModel tbl = new DefaultTableModel();
            tbl.addColumn("No_Surat");
            tbl.addColumn("User_ID");
